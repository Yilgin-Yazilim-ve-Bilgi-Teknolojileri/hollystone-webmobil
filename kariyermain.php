    const formButtons = document.querySelectorAll(".noborderbuton");
    const formContainers = document.querySelectorAll(".form-container");
    const butonmain = document.getElementById("formmain");

    formButtons.forEach((button, index) => {
        button.addEventListener("click", (event) => {
            event.preventDefault(); // Sayfanın varsayılan davranışını engelle
            const formId = button.getAttribute("data-form-id");
            formContainers.forEach(container => {
                container.style.display = "none";
            });
            document.getElementById(formId).style.display = "block";
            butonmain.style.display = "none";
        });
    });

    const subFormButtons = document.querySelectorAll(".formButton");
    const subFormContainers = document.querySelectorAll(".sub-form-container");

    subFormButtons.forEach((button, index) => {
        button.addEventListener("click", (event) => {
