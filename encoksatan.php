<script>
    $(document).ready(function() {
        var slideCount = $(".slideshop").length; // Yorum kartlarının sayısı
        var currentSlide = 0; // Şu anki slaytın indeksi
        var displayCount = 1; // Ekranda görünen yorum sayısı

        // İleri ok düğmesine tıklandığında
        $("#shopileri").click(function() {
            if (currentSlide + displayCount < slideCount) {
                currentSlide++;
                slide();
            }
        });

        // Geri ok düğmesine tıklandığında
        $("#shopgeri").click(function() {
            if (currentSlide > 0) {
                currentSlide--;
                slide();
            }
        });

        // Belirtilen slaytı görüntülemek için bir işlev
        function slide() {
            $(".slideshop").css("display", "none"); // Tüm yorumları gizle
            for (var i = currentSlide; i < currentSlide + displayCount; i++) {
                $(".slideshop:eq(" + i + ")").css("display", "block"); // Görüntülenmesi gereken yorumları göster
            }
        }

        // İlk slaytı görüntülemek için başlat
        slide();
    });
</script>