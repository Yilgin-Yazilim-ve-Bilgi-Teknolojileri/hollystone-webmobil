<script>
    // Get the modal
    var girisyapmodal = document.getElementById("girisyapmodal");
    // Get the button that opens the modal
    var girisyapbuton = document.getElementById("girisyapbuton");
    var girisyapmodalkapat = document.getElementById("girisyapmodalkapat");
    // When the user clicks the button, open the modal 
    girisyapbuton.onclick = function() {
        girisyapmodal.style.display = "block";
    }
    girisyapmodalkapat.onclick = function() {
        girisyapmodal.style.display = "none";
    }
    // When the user clicks anywhere outside of the modal, close it
    window.onclick = function(event) {
        if (event.target == girisyapmodal) {
            girisyapmodal.style.display = "none";
        }
    }

    function openRegistrationModal() {
        var modal = document.getElementById("registrationModal");
        modal.style.display = "block";
    }

    function closeRegistrationModal() {
        var modal = document.getElementById("registrationModal");
        modal.style.display = "none";
    }
</script>