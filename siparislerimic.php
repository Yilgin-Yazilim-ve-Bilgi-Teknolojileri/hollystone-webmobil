    <script>
        document.addEventListener("DOMContentLoaded", function() {
            const detayButtons = document.querySelectorAll(".siparisdetaybuton");
            detayButtons.forEach(function(button) {
                button.addEventListener("click", function() {
                    const modalName = button.getAttribute("data-modal-name");
                    const modal = document.getElementById(modalName + "Modal");

                    if (modal) {
                        modal.style.display = "block";
                    }
                });
            });

            // Modal kapatma düğmesi
            const closeBtns = document.querySelectorAll(".close");
            closeBtns.forEach(function(closeBtn) {
                closeBtn.addEventListener("click", function() {
                    const modal = closeBtn.parentElement.parentElement;
                    modal.style.display = "none";
                });
            });

            // Modal dışına tıklama ile modalı kapatma
            window.addEventListener("click", function(event) {
                const modals = document.querySelectorAll(".modal-style");
                modals.forEach(function(modal) {
                    if (event.target == modal) {
                        modal.style.display = "none";
                    }
                });
            });
        });
    </script>