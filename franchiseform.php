<script>
    document.getElementById('showBannerButton').addEventListener('click', function() {
        var banner = document.getElementById('banner');
        banner.style.display = 'block'; // Banner'ı görünür yap

        setTimeout(function() {
            banner.style.display = 'none'; // 5 saniye sonra banner'ı gizle
        }, 5000);
    });
    const slides = document.querySelectorAll('.franchise .slide');
    const dots = document.querySelectorAll('.franchise .slider-dot');
    let currentSlide = 0;

    function showSlide(slideIndex) {
        slides.forEach((slide) => {
            slide.style.transform = `translateX(-${slideIndex * 100}%)`;
        });

        dots.forEach((dot, index) => {
            if (index === slideIndex) {
                dot.style.backgroundColor = '#333';
            } else {
                dot.style.backgroundColor = '#888';
            }
        });
    }

    function nextSlide() {
        currentSlide = (currentSlide + 1) % slides.length;
        showSlide(currentSlide);
    }

    function prevSlide() {
        currentSlide = (currentSlide - 1 + slides.length) % slides.length;
        showSlide(currentSlide);
    }

    document.getElementById('next-arrow1').addEventListener('click', nextSlide);
    document.getElementById('prev-arrow1').addEventListener('click', prevSlide);
    $(document).ready(function() {
        $(".row3cardbg").click(function() {
            // Tıklanan elementin border rengini değiştir
            $(this).css("border", "1px solid #00731C");

            // Diğer elementlerin border rengini sıfırla
            $(".row3cardbg").not(this).css("border", "1px solid transparent");
        });
    });

    $(document).ready(function() {
        $(".frow14div1, .frow14div2").click(function() {
            // Tıklanan elementin arka plan rengini değiştir
            $(this).css("background-color", "#303030");
            $(this).css("color", "#fff");

            // Diğer elementlerin arka plan rengini sıfırla
            $(".frow14div1, .frow14div2").not(this).css("background-color", "");

            $(".frow14div1, .frow14div2").not(this).css("color", "");
        });
    });
</script>