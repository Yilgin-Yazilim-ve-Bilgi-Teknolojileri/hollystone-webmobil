    document.addEventListener("DOMContentLoaded", function() {
        let currentSlide = 0;
        const slides = document.querySelectorAll(".sahneslider .sahneslide");
        const totalSlides = slides.length;

        function showSlide(index) {
            slides.forEach((slide, i) => {
                slide.style.display = i === index ? "flex" : "none";
            });
        }

        function nextSlide() {
            currentSlide = (currentSlide + 1) % totalSlides;
            showSlide(currentSlide);
        }

        function prevSlide() {
            currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
            showSlide(currentSlide);
        }

        // Initial setup
        showSlide(currentSlide);

        // Event listeners for manual control
        document.getElementById("nextArrow").addEventListener("click", nextSlide);
        document.getElementById("prevArrow").addEventListener("click", prevSlide);
    });
    let slideIndex = 0;
    const slides = document.querySelectorAll('.slide');
    const prevArrow = document.querySelector('.prev-arrow1');
    const nextArrow = document.querySelector('.next-arrow1');

    function showSlide(index) {
        slides.forEach(slide => slide.style.transform = `translateX(-${index * 100}%)`);
    }

    prevArrow.addEventListener('click', () => {
        if (slideIndex > 0) {
            slideIndex--;
            showSlide(slideIndex);
        }
    });

    nextArrow.addEventListener('click', () => {
        if (slideIndex < slides.length - 1) {
            slideIndex++;
            showSlide(slideIndex);
        }
    });

    showSlide(slideIndex);
